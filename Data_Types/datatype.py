#Numeric DataType
mobileno = 6379616860
print('Integer Data Type : ',mobileno,type(mobileno))
temperature = 34.4
print('Float Data Type : ',temperature,type(temperature))
complexnumber = 35+56j
print('Complex Number Data Type : ',complexnumber,type(complexnumber))

#Sequence Data Type
course = 'Python'
print('String Data Type : ',course,type(course))
courses = ['Python','Java','DOT NET']
print('List Data Type : ',courses,type(courses))
courses_ = ('Python','Java','DOT NET')
print('Tuple Data Type : ',courses_,type(courses_))

#Boolean
is_pass = True
print('Boolean DataType : ',is_pass,type(is_pass))

#Set
course2 = {'Python','Java','PHP'}
print('Set DataType : ',course2,type(course2))

#Dictionary
watch_details = {
    'Titan':40000,
    'Sonata':50000
}
print('Dictionary DataType : ',watch_details,type(watch_details))